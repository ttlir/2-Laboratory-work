import json

# Создание словаря с информацией о книге
book_info = {
    'название': 'Мастер и Маргарита',
    'автор': 'Михаил Булгаков',
    'год издания': 1967
}

# Преобразование словаря в строку JSON
json_string = json.dumps(book_info)

# Вывод строки JSON
print("Строка JSON:")
print(json_string)

# Преобразование строки JSON обратно в словарь
decoded_book_info = json.loads(json_string)

# Вывод словаря
print("\nСловарь:")
print(decoded_book_info)

