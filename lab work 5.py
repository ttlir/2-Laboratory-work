def find_drinks_below_price(menu, max_price):
    """
    Функция для поиска напитков с ценами ниже заданной суммы.

    Параметры:
    - menu: словарь меню, где ключи - названия напитков, значения - их цены.
    - max_price: максимальная цена напитка, ниже которой он должен быть выведен.

    Результат:
    - Выводит на экран названия напитков с ценами ниже max_price.
    """
    print(f"Напитки с ценой ниже {max_price}:")
    for drink, price in menu.items():
        if price < max_price:
            print(drink)

# Пример использования функции
menu = {
    'кофе': 100,
    'чай': 50,
    'лимонад': 80,
    'капучино': 120
}
max_price = 90
find_drinks_below_price(menu, max_price)
